char	*strchr(const char *s, int c)
{
	int	i;

	i = 0;
	if (c == 0)
	{
		while (s[i])
			i++;
		return (s[i]);
	}
	while (s[i])
	{
		if (s[i] == c)
			return (s[i]);
		i++;
	}
	return (0);
}
